function add(num1,num2){
    return num1 + num2
}

function subtract(num1, num2){
    return num1 - num2
}

function multiply(num1, num2){
    return num1 * num2
}

function divide(num1,num2){
    return num1 / num2
}

function increment(number){
    let increm = ++number
    return increm
}

function decrement(n){
    let decre = --n
    return decre
}

function makeInt(n){
    let converted = parseInt(n,10)
    return converted
}

function preserveDecimal(n){
    let convo = parseFloat(n)
    return convo
}
